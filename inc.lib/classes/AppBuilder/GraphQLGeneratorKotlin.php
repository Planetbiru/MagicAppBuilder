set -euf
[ "\${MVNW_VERBOSE-}" != debug ] || set -x

# OS specific support.
native_path() { printf %s\\n "\$1"; }
case "\$(uname)" in
CYGWIN* | MINGW*)
  [ -z "\${JAVA_HOME-}" ] || JAVA_HOME="\$(cygpath --unix "\$JAVA_HOME")"
  native_path() { cygpath --path --windows "\$1"; }
  ;;

<?php

namespace AppBuilder;

/**
 * Class ReferenceType
 * 
 * Defines constant values for different reference types.
 */
class ReferenceType
{
    /** @var string Represents an entity reference type */
    const ENTITY = 'entity';
    
    /** @var string Represents a map reference type */
    const MAP = 'map';
    
    /** @var string Represents a yes/no reference type */
    const YESNO = 'yesno';
    
    /** @var string Represents a true/false reference type */
    const TRUE_FALSE = 'truefalse';
    
    /** @var string Represents a one/zero reference type */
    const ONE_ZERO = 'onezero';
}
